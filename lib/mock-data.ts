// Apple demo нэгж бүтээх товч туслах (brand=Apple, зураг manifest-аас).
function unit(
  id: string,
  model: string,
  storage: string,
  color: string,
  condition: PhoneCondition,
  battery_health: number | null,
  price: number,
  created_at: string,
): Phone {
  return {
    id,
    brand: "Apple",
    model,
    storage,
    color,
    condition,
    battery_health,
    price,
    image_url: getColorImage(model, color),
    description: null,
    status: "available",
    created_at,
  };
}

